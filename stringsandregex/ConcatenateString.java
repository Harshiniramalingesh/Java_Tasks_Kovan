package stringsandregex;

public class ConcatenateString {
    public static void main(String[] args) {
		String s1 = "Hello ";
		s1 = s1.concat("world!");
		
		
		
		System.out.println(s1);
		
		String s2 = "Hello";
		s2.concat("World!");
		
		System.out.println(s2);
		
		String s3 = "Harshini " + "Ramalingesh";
		System.out.println(s3);

	}

}
